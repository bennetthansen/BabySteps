package edu.Drake.babysteps;

public class PackingList {
    public String title;
    public String date;
    public String progress;
    public PackingList(){
        super();
    }
    
    public PackingList(String title, String date, String progress) {
        super();
        this.title = title;
        this.date = date;
        this.progress = progress;
    }
}
