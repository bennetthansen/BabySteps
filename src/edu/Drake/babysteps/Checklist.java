package edu.Drake.babysteps;

public class Checklist {
    public String title;
    public String date;
    public String progress;
    public Checklist(){
        super();
    }
    
    public Checklist(String title, String date, String progress) {
        super();
        this.title = title;
        this.date = date;
        this.progress = progress;
    }
}
